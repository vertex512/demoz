uint8_t g_buf[4096];
uint8_t g_buf2[4096];

void _test_p521(struct test_vector *t, int32_t n)
{
	printf("case %d vector:\n", n);
	print_vector(t);

	F_SYMBOL(ecdsa_p521_public_key)(t->in_pri, g_buf);

	printf("out_pub: %u -- %s\n ", t->out_pub_len,
		verify_cmp(g_buf, t->out_pub, t->out_pub_len));
	print_hex(g_buf, t->out_pub_len);

	F_SYMBOL(ecdsa_p521_sign)(t->in_pri, t->in_ran,
		t->in_msg, t->in_msg_len,
		g_buf2);

	printf("out_sign: %u -- %s\n ", t->out_sign_len,
		verify_cmp(g_buf2, t->out_sign, t->out_sign_len));
	print_hex(g_buf2, t->out_sign_len);

	int32_t r = F_SYMBOL(ecdsa_p521_verify)(g_buf, g_buf2,
		t->in_msg, t->in_msg_len);

	printf("verify -- %s\n", verify_ieq(r));

	printf("\n");
}

void test_ecdsa_p521(void)
{
	printf("==== ECDSA P-521 ====\n\n");

	_test_p521(&test_p521_vector_1, 1);
	_test_p521(&test_p521_vector_2, 2);
}

int main(void)
{
	test_ecdsa_p521();

	return 0;
}
