uint8_t g_buf[4096];
uint8_t g_a_pub[128];
uint8_t g_b_pub[128];

void _test_x448(struct test_vector *t, int32_t n)
{
	printf("case %d vector:\n", n);
	print_vector(t);

	F_SYMBOL(ecdh_x448_public_key)(t->in_a_pri, g_a_pub);
	F_SYMBOL(ecdh_x448_public_key)(t->in_b_pri, g_b_pub);

	printf("out_a_pub: %u -- %s\n ", t->out_a_pub_len,
		verify_cmp(g_a_pub, t->out_a_pub, t->out_a_pub_len));
	print_hex(g_a_pub, t->out_a_pub_len);

	printf("out_b_pub: %u -- %s\n ", t->out_b_pub_len,
		verify_cmp(g_b_pub, t->out_b_pub, t->out_b_pub_len));
	print_hex(g_b_pub, t->out_b_pub_len);

	F_SYMBOL(ecdh_x448_shared_key)(t->in_a_pri, g_b_pub, g_buf);

	printf("out_a_key: %u -- %s\n ", t->out_key_len,
		verify_cmp(g_buf, t->out_key, t->out_key_len));
	print_hex(g_buf, t->out_key_len);

	F_SYMBOL(ecdh_x448_shared_key)(t->in_b_pri, g_a_pub, g_buf);

	printf("out_b_key: %u -- %s\n ", t->out_key_len,
		verify_cmp(g_buf, t->out_key, t->out_key_len));
	print_hex(g_buf, t->out_key_len);

	printf("\n");
}

void _test_x448_u(struct test_vector_u *t, int32_t n)
{
	printf("case %d vector:\n", n);
	print_vector_u(t);

	F_SYMBOL(ecdh_x448_shared_key)(t->in_s, t->in_u, g_buf);

	printf("pub: %u -- %s\n ", t->out_u_len,
		verify_cmp(g_buf, t->out_u, t->out_u_len));
	print_hex(g_buf, t->out_u_len);

	printf("\n");
}

void test_x448(void)
{
	printf("==== X448 ====\n\n");

	_test_x448(&test_x448_vector_1, 1);
}

void test_x448_u(void)
{
	printf("==== X448 u-coordinate ====\n\n");

	_test_x448_u(&test_x448_u_vector_1, 1);
	_test_x448_u(&test_x448_u_vector_2, 2);
}

