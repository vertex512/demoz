void print_hex(const uint8_t *buf, uint32_t n)
{
	printf(" ");
	for (uint32_t i = 0; i < n; i++)
		printf("%02x", buf[i]);
	printf("\n");

	for (uint32_t i = 0; i < n; i++) {
		printf(" 0x%02x", buf[i]);
		if ((i + 1) != n)
			printf(",");
		if (!((i + 1) % 8))
			printf("\n");
	}
	if (n % 8)
		printf("\n");
}

const char *verify_cmp(const uint8_t *a, const uint8_t *b, uint32_t len)
{
	return C_SYMBOL(memcmp)(a, b, len) ? "No" : "Yes";
}

const char *verify_ieq(int32_t r)
{
	return r ? "No" : "Yes";
}

