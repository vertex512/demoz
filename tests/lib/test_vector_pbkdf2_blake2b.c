void _test_pbkdf2_blake2b(struct test_vector *t, int32_t n)
{
	printf("case %d vector:\n", n);
	print_vector(t);

	F_SYMBOL(pbkdf2_blake2b)(
		t->in_pass, t->in_pass_len,
		t->in_salt, t->in_salt_len,
		g_buf, t->out_odk_len,
		t->in_iter);

	printf("out_odk: %u -- %s\n ", t->out_odk_len,
		verify_cmp(g_buf, t->out_odk, t->out_odk_len));
	print_hex(g_buf, t->out_odk_len);

	printf("\n");
}

void test_pbkdf2_blake2b(void)
{
	printf("==== PBKDF2 BLAKE2B ====\n\n");

	_test_pbkdf2_blake2b(&test_blake2b_vector_1, 1);
	_test_pbkdf2_blake2b(&test_blake2b_vector_2, 2);
	_test_pbkdf2_blake2b(&test_blake2b_vector_3, 3);
	_test_pbkdf2_blake2b(&test_blake2b_vector_4, 4);
}

