void print_vector(struct test_vector *v)
{
	printf("vector in_ikm: %u\n ", v->in_ikm_len);
	print_hex(v->in_ikm, v->in_ikm_len);
	printf("vector in_salt: %u\n ", v->in_salt_len);
	print_hex(v->in_salt, v->in_salt_len);
	printf("vector in_info: %u\n ", v->in_info_len);
	print_hex(v->in_info, v->in_info_len);
	printf("vector out_okm: %u\n ", v->out_okm_len);
	print_hex(v->out_okm, v->out_okm_len);
}

