uint8_t g_buf[4096];

void _test_xsalsa20(struct test_vector *t, int32_t n)
{
	printf("case %d vector:\n", n);
	print_vector(t);

	XSALSA20_NEW(ctx);

	F_SYMBOL(xsalsa20_init)(&ctx, t->in_key, t->in_ran, t->in_crt);

	C_SYMBOL(memcpy)(g_buf, t->in_data, t->in_data_len);
	F_SYMBOL(xsalsa20_crypto)(&ctx, g_buf, t->in_data_len);

	printf("out_data cipher: %u -- %s\n ", t->out_data_len,
		verify_cmp(g_buf, t->out_data, t->out_data_len));
	print_hex(g_buf, t->out_data_len);

	F_SYMBOL(xsalsa20_init)(&ctx, t->in_key, t->in_ran, t->in_crt);

	F_SYMBOL(xsalsa20_crypto)(&ctx, g_buf, t->out_data_len);

	printf("in_data plain: %u -- %s\n ", t->in_data_len,
		verify_cmp(g_buf, t->in_data, t->in_data_len));
	print_hex(g_buf, t->in_data_len);

	printf("\n");
}

void test_xsalsa20(void)
{
	printf("==== XSalsa20 ====\n\n");

	_test_xsalsa20(&test_xsalsa20_vector_1, 1);
}

