uint8_t g_dp[4096];
uint8_t g_dq[4096];
uint8_t g_qinv[4096];

void _test_rsa2048(struct test_vector *t, int32_t n)
{
	printf("case %d vector:\n", n);
	print_vector(t);

	F_SYMBOL(rsa2048_genkey)(t->in_p, t->in_q,
		g_e, g_d, g_n);

	printf("out_e: %u -- %s\n ", t->out_e_len,
		verify_cmp(g_e, t->out_e, t->out_e_len));
	print_hex(g_e, t->out_e_len);

	printf("out_d: %u -- %s\n ", t->out_d_len,
		verify_cmp(g_d, t->out_d, t->out_d_len));
	print_hex(g_d, t->out_d_len);

	printf("out_n: %u -- %s\n ", t->out_n_len,
		verify_cmp(g_n, t->out_n, t->out_n_len));
	print_hex(g_n, t->out_n_len);

	C_SYMBOL(memcpy)(g_buf, t->in_msg, t->in_msg_len);
	F_SYMBOL(rsa2048_encrypt)(g_e, g_n, g_buf);

	printf("out_enc: %u\n ", t->in_msg_len);
	print_hex(g_buf, t->in_msg_len);

	F_SYMBOL(rsa2048_decrypt)(g_d, g_n, g_buf);

	printf("out_dec: %u -- %s\n ", t->in_msg_len,
		verify_cmp(g_buf, t->in_msg, t->in_msg_len));
	print_hex(g_buf, t->in_msg_len);

	/* s = h^d mod n */
	/* h = s^e mod n */

	F_SYMBOL(rsa2048_decrypt)(g_d, g_n, g_buf);

	printf("out_sign: %u\n ", t->in_msg_len);
	print_hex(g_buf, t->in_msg_len);

	F_SYMBOL(rsa2048_encrypt)(g_e, g_n, g_buf);

	printf("out_verify: %u -- %s\n ", t->in_msg_len,
		verify_cmp(g_buf, t->in_msg, t->in_msg_len));
	print_hex(g_buf, t->in_msg_len);

	/* crt */

	F_SYMBOL(rsa2048_gencrt)(t->in_p, t->in_q, g_d,
		g_dp, g_dq, g_qinv);

	printf("out_dp: %u -- %s\n ", t->out_dp_len,
		verify_cmp(g_dp, t->out_dp, t->out_dp_len));
	print_hex(g_dp, t->out_dp_len);

	printf("out_dq: %u -- %s\n ", t->out_dq_len,
		verify_cmp(g_dq, t->out_dq, t->out_dq_len));
	print_hex(g_dq, t->out_dq_len);

	printf("out_qinv: %u -- %s\n ", t->out_qinv_len,
		verify_cmp(g_qinv, t->out_qinv, t->out_qinv_len));
	print_hex(g_qinv, t->out_qinv_len);

	F_SYMBOL(rsa2048_encrypt)(g_e, g_n, g_buf);
	F_SYMBOL(rsa2048_crt_decrypt)(t->in_p, t->in_q,
		g_dp, g_dq, g_qinv, g_buf);

	printf("out_crt_dec: %u -- %s\n ", t->in_msg_len,
		verify_cmp(g_buf, t->in_msg, t->in_msg_len));
	print_hex(g_buf, t->in_msg_len);

	printf("\n");
}

