void print_hex(const uint8_t *buf, uint32_t n)
{
	printf(" ");
	for (uint32_t i = 0; i < n; i++)
		printf("%02x", buf[i]);
	printf("\n");

	for (uint32_t i = 0; i < n; i++) {
		printf(" 0x%02x", buf[i]);
		if ((i + 1) != n)
			printf(",");
		if (!((i + 1) % 8))
			printf("\n");
	}
	if (n % 8)
		printf("\n");
}

const char *verify_cmp(const uint8_t *a, const uint8_t *b, uint32_t len)
{
	return C_SYMBOL(memcmp)(a, b, len) ? "No" : "Yes";
}

struct test_vector {
	uint8_t in_key[128];
	uint32_t in_key_len;
	uint8_t in_ran[128];
	uint32_t in_ran_len;
	uint8_t in_crt[128];
	uint32_t in_crt_len;
	uint8_t in_data[128];
	uint32_t in_data_len;
	uint8_t out_data[128];
	uint32_t out_data_len;
};

void print_vector(struct test_vector *v)
{
	printf("vector in_key: %u\n ", v->in_key_len);
	print_hex(v->in_key, v->in_key_len);
	printf("vector in_ran: %u\n ", v->in_ran_len);
	print_hex(v->in_ran, v->in_ran_len);
	printf("vector in_crt: %u\n ", v->in_crt_len);
	print_hex(v->in_crt, v->in_crt_len);
	printf("vector in_data: %u\n ", v->in_data_len);
	print_hex(v->in_data, v->in_data_len);
	printf("vector out_data: %u\n ", v->out_data_len);
	print_hex(v->out_data, v->out_data_len);
}

