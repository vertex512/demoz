uint8_t g_buf[4096];

void _test_xchacha20(struct test_vector *t, int32_t n)
{
	printf("case %d vector:\n", n);
	print_vector(t);

	XCHACHA20_NEW(ctx);

	F_SYMBOL(xchacha20_init)(&ctx, t->in_key, t->in_ran, t->in_crt);

	C_SYMBOL(memcpy)(g_buf, t->in_data, t->in_data_len);
	F_SYMBOL(xchacha20_crypto)(&ctx, g_buf, t->in_data_len);

	printf("out_data cipher: %u -- %s\n ", t->out_data_len,
		verify_cmp(g_buf, t->out_data, t->out_data_len));
	print_hex(g_buf, t->out_data_len);

	F_SYMBOL(xchacha20_init)(&ctx, t->in_key, t->in_ran, t->in_crt);

	F_SYMBOL(xchacha20_crypto)(&ctx, g_buf, t->out_data_len);

	printf("in_data plain: %u -- %s\n ", t->in_data_len,
		verify_cmp(g_buf, t->in_data, t->in_data_len));
	print_hex(g_buf, t->in_data_len);

	printf("\n");
}

void test_xchacha20(void)
{
	printf("==== XChaCha20 ====\n\n");

	_test_xchacha20(&test_xchacha20_vector_1, 1);
}

