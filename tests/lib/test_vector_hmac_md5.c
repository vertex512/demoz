	_test_hmac_md5(&test_md5_vector_1, 1);
	_test_hmac_md5(&test_md5_vector_2, 2);
	_test_hmac_md5(&test_md5_vector_3, 3);
	_test_hmac_md5(&test_md5_vector_4, 4);
