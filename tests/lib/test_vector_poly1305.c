void print_vector(struct test_vector *v)
{
	printf("vector in_key: %u\n ", v->in_key_len);
	print_hex(v->in_key, v->in_key_len);
	printf("vector in_msg: %u\n ", v->in_msg_len);
	print_hex(v->in_msg, v->in_msg_len);
	printf("vector out_tag: %u\n ", v->out_tag_len);
	print_hex(v->out_tag, v->out_tag_len);
}

