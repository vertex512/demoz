uint8_t g_buf[4096];

void _test_poly1305(struct test_vector *t, int32_t n)
{
	printf("case %d vector:\n", n);
	print_vector(t);

	POLY1305_NEW(ctx);

	F_SYMBOL(poly1305_init)(&ctx, t->in_key);
	F_SYMBOL(poly1305_process)(&ctx, t->in_msg, t->in_msg_len);
	F_SYMBOL(poly1305_finish)(&ctx);

	C_SYMBOL(memcpy)(g_buf, &POLY1305_TAG(&ctx, 0), POLY1305_TAG_LEN);

	printf("out_tag: %u -- %s\n ", t->out_tag_len,
		verify_cmp(g_buf, t->out_tag, t->out_tag_len));
	print_hex(g_buf, t->out_tag_len);

	printf("\n");
}

