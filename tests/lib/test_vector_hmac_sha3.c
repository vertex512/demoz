void _test_hmac_sha3(struct test_vector *t, int32_t n, int32_t sha3_type)
{
	printf("case %d vector:\n", n);
	print_vector(t);

	HMAC_SHA3_NEW(ctx);

	F_SYMBOL(hmac_sha3_init)(&ctx,
		t->in_key, t->in_key_len,
		sha3_type);
	F_SYMBOL(hmac_sha3_process)(&ctx,
		t->in_data, t->in_data_len);
	F_SYMBOL(hmac_sha3_finish)(&ctx);

	printf("out_dig: %u -- %s\n ", t->out_dig_len,
		verify_cmp(&HMAC_SHA3_STATE(&ctx, 0),
			t->out_dig, t->out_dig_len));
	print_hex(&HMAC_SHA3_STATE(&ctx, 0), t->out_dig_len);

	printf("\n");
}

