void _test_hmac_blake2b(struct test_vector *t, int32_t n)
{
	printf("case %d vector:\n", n);
	print_vector(t);

	HMAC_BLAKE2B_NEW(ctx);

	F_SYMBOL(hmac_blake2b_init)(&ctx,
		t->in_key, t->in_key_len);
	F_SYMBOL(hmac_blake2b_process)(&ctx,
		t->in_data, t->in_data_len);
	F_SYMBOL(hmac_blake2b_finish)(&ctx);

	printf("out_dig: %u -- %s\n ", t->out_dig_len,
		verify_cmp(&HMAC_BLAKE2B_STATE(&ctx, 0),
			t->out_dig, t->out_dig_len));
	print_hex(&HMAC_BLAKE2B_STATE(&ctx, 0), t->out_dig_len);

	printf("\n");
}

